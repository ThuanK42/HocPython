# Tuples()

tup1 = (1, 2, 3, 4, 5, 6, 6)
print(type(tup1), tup1)

tup2 = ("Le", "Van", "Thuan")
print(type(tup2), tup2)

tup3 = (1, "Le", "Van", "Thuan", 3)
print(type(tup3), tup3)

# Lists[]

list1 = [1, 2, 3, 4, 5, "a", "b", "c"]
print(type(list1), list1)

# sets{} no se sap xep cac phan tu tang dan san luon
