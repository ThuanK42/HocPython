'''MENH DE DIEU KIEN IF IF ELSE '''

''' IF '''
x = 20
y = 30
'''sau if 1 dong lenh '''
if x > y:
    print(x, 'lớn hơn', y)
print('OK, bro')

'''sau if nhieu dong lenh '''
if x > y:
    print(x, 'lớn hơn', y)
    print(x, 'lớn hơn', y)
    print(x, 'lớn hơn', y)
print('OK, bro2')

'''truong hop dung'''
if x < y:
    print(x, 'bé hơn', y)
    print(x, 'bé hơn', y)
    print(x, 'bé hơn', y)
print('OK, bro3')

''' IF ELSE '''

x = 10
y = 8
if x > y:
    print(x, 'lớn hơn', y)
else:
    print(x, 'bé hơn', y)

